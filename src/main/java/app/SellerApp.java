    public void fetchOneSeller(){
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/seller/get/" + checkSession.getId()))
                    .header("Content-Type", "application/json")
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            Gson gson = new Gson();
            Seller seller = gson.fromJson(getResponse.body(), Seller.class);
            
            if(seller != null){
                nameInput.setText(seller.getFullName());
                addressInput.setText(seller.getAddress());
                emailInput.setText(seller.getEmail());
                phoneNumberInput.setText(seller.getPhoneNumber());
            }
            
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    public void fetchSellerProducts(){
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/seller/get/product/" + checkSession.getId()))
                    .header("Content-Type", "application/json")
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            Gson gson = new Gson();
            Product[] products = gson.fromJson(getResponse.body(), Product[].class);
            
            menuListModel.clear();
            if(products != null){
                for (Product product : products) {
                    long productId = product.getProductId();
                    String productName = product.getProductName();
                    menuListModel.addElement(productId + "-" + productName);
                }
            }
            
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
