    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        Product product = new Product();
        product.setProductName(productNameInput.getText());
        product.setProductDescription(productDescriptionInput.getText());
        product.setProductUnitPrice((int) productPriceInput.getValue());
        product.setProductStock((int) productStockInput.getValue());
        product.setFood(radioBtnHandler());
        
        Gson gson = new Gson();
        String jsonRequest = gson.toJson(product);
        
        try {
            HttpRequest postRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/product/insert?sellerId=" + checkSession.getId()))
                    .header("Content-Type", "application/json")
                    .POST(HttpRequest.BodyPublishers.ofString(jsonRequest))
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> postResponse = httpClient.send(postRequest, HttpResponse.BodyHandlers.ofString());
            
            if(postResponse.statusCode() == 201){
                JOptionPane.showMessageDialog(this, "Berhasil Menambahkan Produk");
                clearForm();
                fetchSellerProducts();
            }
            else {
                throw new Exception("Tambah Produk Gagal!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Tambah Produk Gagal!");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

