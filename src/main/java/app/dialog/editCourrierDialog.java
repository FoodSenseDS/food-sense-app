     private void fetchOneCourrier() {
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/courier/get/" + Id))
                    .header("Content-Type", "application/json")
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            Gson gson = new Gson();
            Courrier courrier = gson.fromJson(getResponse.body(), Courrier.class);
            
            if(courrier != null){
               courrierNameInput.setText(courrier.getFullName());
               emailInput.setText(courrier.getEmail());
               phoneInput.setText(courrier.getPhone_number());
               
               if(courrier.isOn_delivery()){
                   statusInput.setText("Sedang Mengantarkan");
               }
               else {
                   statusInput.setText("Sedang Tidak Mengantarkan");
               }
            }
            
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
