    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        Courrier courrier = new Courrier();
        courrier.setFullName(courrierNameInput.getText());
        courrier.setEmail(emailInput.getText());
        courrier.setPhone_number(phoneInput.getText());
        
        if("Sedang Mengantarkan".equals(statusInput.getText())){
            courrier.setOn_delivery(true);
        }
        else {
            courrier.setOn_delivery(false);
        }
        
        Gson gson = new Gson();
        String jsonRequest = gson.toJson(courrier);
        
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/courier/update/" + Id))
                    .header("Content-Type", "application/json")
                    .PUT(HttpRequest.BodyPublishers.ofString(jsonRequest))
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            if(getResponse.statusCode() == 200 || getResponse.body().equals("Courier successfully updated")){
                JOptionPane.showMessageDialog(this, "Data Courrier Berhasil Diupdate");
            }
            else {
                System.out.println(getResponse.body());
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

