    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        Seller seller = new Seller();
        seller.setFullName(sellerNameInput.getText());
        seller.setAddress(sellerAddressInput.getText());
        seller.setEmail(sellerEmailInput.getText());
        seller.setPhoneNumber(sellerPhoneInput.getText());
        
        Gson gson = new Gson();
        String jsonRequest = gson.toJson(seller);
        
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/seller/update/" + Id))
                    .header("Content-Type", "application/json")
                    .PUT(BodyPublishers.ofString(jsonRequest))
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            if(getResponse.statusCode() == 200 || getResponse.body().equals("Seller successfully updated")){
                JOptionPane.showMessageDialog(this, "Data Seller Berhasil Diupdate");
            }
            else {
                System.out.println(getResponse.body());
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

