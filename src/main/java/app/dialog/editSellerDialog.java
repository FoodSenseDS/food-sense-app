    public void fetchOneSeller(){
        try {
            HttpRequest getRequest = HttpRequest.newBuilder()
                    .uri(new URI("https://foodsenseapi-demo.azuremicroservices.io/foodsense/api/v0.0.1/seller/get/" + Id))
                    .header("Content-Type", "application/json")
                    .build();
            
            HttpClient httpClient = HttpClient.newHttpClient();
            HttpResponse<String> getResponse = httpClient.send(getRequest, HttpResponse.BodyHandlers.ofString());
            
            Gson gson = new Gson();
            Seller seller = gson.fromJson(getResponse.body(), Seller.class);
            
            if(seller != null){
                sellerNameInput.setText(seller.getFullName());
                sellerAddressInput.setText(seller.getAddress());
                sellerEmailInput.setText(seller.getEmail());
                sellerPhoneInput.setText(seller.getPhoneNumber());
            }
            
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
